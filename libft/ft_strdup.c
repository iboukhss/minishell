#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*new_s;
	size_t	i;
	size_t	strlen;

	i = 0;
	strlen = ft_strlen(s);
	new_s = malloc(sizeof(char) * (strlen + 1));
	if (new_s == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		new_s[i] = s[i];
		i++;
	}
	new_s[i] = '\0';
	return (new_s);
}
